     */
    public function store(Request $request)
    {
        $user = Auth::user();
        $team = Team::create([
            'team_name' => $request['team_name'],
            'game' => $request['game'],
            'num_of_members' => 1,
            'team_leader' => $user->name,
            'team_description' => $request['team_description'],
            'user_id' => Auth::id()
        ]);
        return redirect('teams');

    }

