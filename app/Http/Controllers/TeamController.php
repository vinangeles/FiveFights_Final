     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $user = Auth::user();
        $admin = false;

        $teams = Team::all();

        if ($user != null) {
            if ($user->isAdmin) {
                $admin = true;

            }
            $teams = Team::where('user_id', '=', $user->id)->get();
        }

        return view('teams', ['teams' => $teams, 'admin' => $admin]);
    }

